num1 = int(input("Ingrese un numero entero"))
num2 = int(input("Ingrese otro numero entero"))

operacion = num1 + num2 #Suma
print(f"Suma: {operacion}")

operacion = num2 - num1 #Resta
print(f"Resta: {operacion}")

operacion = num1 * num2 #Mullti
print(f"Multiplicacion: {operacion}")

operacion = num2 // num1 #Division entera
print(f"Division: {operacion}")

operacion = num2 % num1 #Resto division
print(f"Resto: {operacion}")